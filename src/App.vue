<script setup>
import { ref } from 'vue'

// give each todo a unique id
let id = 0

const newTodo = ref('')
const todos = ref([
  { id: id++, text: 'Learn HTML' },
  { id: id++, text: 'Learn JavaScript' },
  { id: id++, text: 'Learn Vue' }
])

function addTodo() {
  todos.value.push({ id: id++, text: newTodo.value })
  newTodo.value = ''
}

function removeTodo(todo) {
  todos.value = todos.value.filter((t) => t !== todo)
}
</script>

<template>
   <form @submit.prevent="addTodo" style="color:blue;font-size:28px;">
    <input v-model="newTodo">
    <button style="color:forestgreen;background-color:yellow;">Add Todo</button>    
  </form>
  <ul>
    <li v-for="todo in todos" :key="todo.id" style="color:blue;font-size:28px;">
      {{ todo.text }}
      <button @click="removeTodo(todo)"
        style="color:red;background-color:lightpink;">X</button>
    </li>
  </ul>
</template>

